import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("../", import.meta.url));
const apiBase = process.env.KTANE_API_BASE_URL ?? "http://localhost:8080";
const [catalogResponse, registrySource, typesSource, twitchSource] = await Promise.all([
  fetch(`${apiBase}/api/modules`),
  readFile(`${root}ktanesolver-frontend/src/components/solvers/registry.ts`, "utf8"),
  readFile(`${root}ktanesolver-frontend/src/types/index.ts`, "utf8"),
  readFile(`${root}ktanesolver-frontend/src/utils/twitchCommands.ts`, "utf8"),
]);

if (!catalogResponse.ok) {
  throw new Error(`Module catalog returned ${catalogResponse.status}`);
}

const catalog = await catalogResponse.json();
if (!Array.isArray(catalog)) throw new Error("Module catalog must be an array");

const matches = (source, pattern) => new Set([...source.matchAll(pattern)].map((match) => match[1]));
const uiTypes = matches(registrySource, /\[ModuleType\.([A-Z0-9_]+)\]/g);
const frontendTypes = matches(typesSource, /^\s*([A-Z][A-Z0-9_]*)\s*=\s*"[A-Z0-9_]+"/gm);
const conditionalBlock = twitchSource.match(/const conditional = new Set<ModuleType>\(\[([\s\S]*?)\]\);/)?.[1] ?? "";
const conditionalTypes = matches(conditionalBlock, /ModuleType\.([A-Z0-9_]+)/g);

const categories = [
  ["VANILLA_REGULAR", "Vanilla regular"],
  ["VANILLA_NEEDY", "Vanilla needy"],
  ["MODDED_REGULAR", "Modded regular"],
  ["MODDED_NEEDY", "Modded needy"],
];

const cell = (value) => String(value ?? "").replaceAll("|", "\\|").replaceAll(/\s+/g, " ").trim();
const contract = (module) => {
  if (module.hasInput && module.hasOutput) return "Input → output";
  if (module.hasInput) return "Input only";
  if (module.hasOutput) return "Output only";
  return "No input/output";
};

const lines = [
  "<!-- Generated by scripts/generate-supported-modules.mjs. Do not edit by hand. -->",
  "# Supported modules",
  "",
  `KTANESolver currently exposes **${catalog.length} registered module solvers** through the live backend catalog.`,
  "",
  "{% hint style=\"info\" %}",
  "This page is generated from `/api/modules`, the frontend solver registry, and the Twitch support map. Start the backend, then run `node scripts/generate-supported-modules.mjs` after adding or changing a module.",
  "{% endhint %}",
  "",
  "## Status legend",
  "",
  "| Column | Meaning |",
  "|---|---|",
  "| UI | A custom frontend solver is registered |",
  "| Twitch: Verified | The generator has an exact audited command fixture |",
  "| Twitch: Conditional | Extra physical or stage context is required before emitting a command |",
  "| Check first | The solve screen highlights the module for early attention |",
  "",
];

for (const [category, title] of categories) {
  const modules = catalog
    .filter((module) => module.category === category)
    .sort((left, right) => left.name.localeCompare(right.name));
  if (!modules.length) continue;

  lines.push(`## ${title} (${modules.length})`, "", "| Module | Type | Contract | UI | Twitch | Check first |", "|---|---|---|:---:|---|:---:|");
  for (const module of modules) {
    const twitch = !frontendTypes.has(module.type)
      ? "Not audited"
      : conditionalTypes.has(module.type) ? "Conditional" : "Verified";
    lines.push(`| ${cell(module.name)} | \`${cell(module.type)}\` | ${contract(module)} | ${uiTypes.has(module.type) ? "Yes" : "No"} | ${twitch} | ${module.checkFirst ? "Yes" : "—"} |`);
  }
  lines.push("");
}

await writeFile(`${root}docs/supported-modules.md`, `${lines.join("\n")}\n`);
console.log(`Wrote docs/supported-modules.md with ${catalog.length} modules`);

